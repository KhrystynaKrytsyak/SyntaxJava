package com.syntax.class04;

public class MoreExamples {

	public static void main(String[] args) {
		
		boolean mothersDay=true;
		
		if (mothersDay==true) {
			System.out.println("Happy Mother's day to all Moms");
		}else {
			System.out.println("Hello");
		}
		System.out.println("____________________________________________________________________");
		
		boolean rain=false;
		
		if (rain) {
			System.out.println("I will take an umbrella");
		}else {
			System.out.println("No need for umbrella");
		
		System.out.println("_______________________________________________________________________");
		
		boolean breakTime=true;
		
		if(breakTime) {
			System.out.println("Let's take a break"); //this statement will get executed because condition is true
		}else {
			System.out.println("Let's continue the class");
		}
		
		}
	}

}
