package com.syntax.reviewclass2;

public class OperatorsDemo {

	public static void main(String[] args) {
		//Operators in Java Arithmetic Operators + - / * % Logical Operator == != >= <= > <
		
		int num=10;
		int num2=10;
		System.out.println(num+num2);
		System.out.println(num>=num2); //num>num2==>False OR num=num2==>True f+t=t
		//Whole 0-->infinity Negative Infinity to Positive Infinity
		System.out.println(Long.MAX_VALUE); //gives us the maximum range. This helps when you dont remember the actual number
		System.out.println(Long.MIN_VALUE); //gives us the minimum range
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Integer.MAX_VALUE);
		System.out.println(num/num2);
		System.out.println(num%num2);
		
		System.out.println(10/3);
		System.out.println(10%3);
		
		System.out.println(10.0/3);
		System.out.println((int)10.0/3);
		System.out.println((int)10.5*3);
		
		
		
		
		
	}
}
