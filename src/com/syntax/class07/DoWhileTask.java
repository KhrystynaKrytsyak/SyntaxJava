package com.syntax.class07;

public class DoWhileTask {

	public static void main(String[] args) {
		//print numbers 1 to 30 using do while
		 int number=1;
				 do {
					 System.out.print(number+" ");
					 number++;
				 }while (number<=30);
		 
	
		 
		 System.out.println();
		// print even numbers from 70 to 30 
		 int num=70;
		 do {
			 System.out.print(num+" ");
			 num-=2;
		 }while (num>=30);		 
		}
	}	

