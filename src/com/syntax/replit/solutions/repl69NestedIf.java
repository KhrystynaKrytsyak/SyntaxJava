package com.syntax.replit.solutions;

public class repl69NestedIf  {
	public static void main (String[] args) {

	for (int x = 1; x < 8; x++) {
		for (int y = 1; y <= 8 - x; y++) {
			System.out.print(y + " ");
		}
		System.out.println();
	}
	for (int x = 1; x < 7; x++) {
		for (int y = 1; y < x + 2; y++) {
			System.out.print(y + " ");
		}
		System.out.println();
	}
		}
	}

